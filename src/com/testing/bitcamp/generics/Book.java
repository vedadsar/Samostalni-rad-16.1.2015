package com.testing.bitcamp.generics;

public class Book {
	
	private String name;
	private String autor;
	private int pages;
	private int numOfChars;
	
	public Book(String name, String autor, int pages, int numOfChars) {
		this.name = name;
		this.autor = autor;
		this.pages = pages;
		this.numOfChars = numOfChars;
	}
	
	
}
